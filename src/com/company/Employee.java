package com.company;

public class Employee {

    public String firstName;
    public String lastName;
    public String id;
    public double salary;
    public double monthlySalary;
    public double twiceAMonthSalary;
    public double everyTwoWeeksSalary;

    public Employee(String firstName, String lastName, String id, double salary, double monthlySalary, double twiceAMonthSalary, double everyTwoWeeksSalary){

        this.firstName = firstName;
        this.lastName = lastName;
        this.id = id;
        this.salary = salary;
        this.monthlySalary = monthlySalary;
        this.twiceAMonthSalary = twiceAMonthSalary;
        this.everyTwoWeeksSalary = everyTwoWeeksSalary;



    }


}
